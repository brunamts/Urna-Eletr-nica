package Model;

/**
 *
 * @author letic
 */
public class Arquivos {
    private String nome;
    private String hash;

    public Arquivos(String nome, String hash) {
        this.nome = nome;
        this.hash = hash;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getHash() {
        return hash;
    }

    public void setHash(String hash) {
        this.hash = hash;
    }

    @Override
    public String toString() {
        return "Arquivos{" + "nome=" + nome + ", hash=" + hash + '}';
    }
}
